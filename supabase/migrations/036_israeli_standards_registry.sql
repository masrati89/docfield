CREATE TABLE israeli_standards (
    id               UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    standard_number  TEXT NOT NULL,
    part             TEXT,
    title            TEXT NOT NULL,
    full_key         TEXT GENERATED ALWAYS AS (
        standard_number || COALESCE('.' || part, '')
    ) STORED,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE UNIQUE INDEX idx_israeli_standards_full_key ON israeli_standards(full_key);

COMMENT ON TABLE  israeli_standards              IS 'Registry of Israeli construction standards (ת"י) for PDF standard text resolution';
